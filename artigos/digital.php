                    <p class="font-article">&nbsp;&nbsp;&nbsp;&nbsp;Se você assiste as notícias ou vem lendo bastante sobre tecnologia e empreendedorismo, provavelmente, já escutou sobre transformação digital. Falando sobre isso a primeira dúvida que normalmente aparece é: o que é transformação digital, afinal?<br>
	&nbsp;&nbsp;&nbsp;&nbsp;Falar sobre essa transformação global é falar sobre a Quarta Revolução Industrial que, diferente das outras revoluções, vem trazendo novas tecnologias com velocidade, alcance e impacto em sistemas nunca vistos. Com intensa automatização que só foi possibilitada pela internet e pelo armazenamento em nuvem.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;Assim a transformação digital vem mudando o mercado e a forma como as empresas precisam se adaptar a ela para permanecer vivas. O famoso adapte-se ou morra.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;Na era digital existem três categorias de negócios que podem ser utilizadas dentro da sua empresa quando o assunto é transformação digital:</p><br><ul class="font-article" style="font-weight: bold; list-style: none;"> 
<li>1) Negócios não-digitais os quais necessitam de espaços físicos enormes, é utilizado o estoque, normalmente faz uso de uma gestão tradicional e utiliza a tecnologia como acessório ao seu serviço ou produto. A tecnologia é parte que dá suporte aos processos e a gestão da empresa, sempre de forma pontual.
<li>2) Quando o seu serviço prestado ou o produto é traduzido para o digital como o Spotify e os e-commerces fazem. O digital se torna um canal de vendas e de marketing para sua empresa. Essa tradução é utilizada nas partes que movem a empresa, em geral, comunicando melhor com seus clientes, potencializando seu alcance, além de fortalecer e disseminar mais sua marca. Uma rede social ou um site potencializam aquele negócio que poderia se manter tradicional
<li>3) Empresas nativas digitais que tem como core, ou seja, como principal produto ou serviço prestado o digital no centro. O digital não permanece apenas em vendas e marketing, mas é transversal a todos os setores da empresa, não como acessório, mas como motor principal de desenvolvimento em gestão, processos, desenvolvimento de produto, entre outros.</ul><br>
