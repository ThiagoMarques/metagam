	&nbsp;&nbsp;&nbsp;&nbsp;Google Drive, Dropbox, iCloud, e tantas outras plataformas que estão sendo desenvolvidas, tem sido os principais espaços de armazenamento de dados pessoais e corporativas. Há quanto tempo você não toca em uma foto impressa? A nuvem revolucionou até mesmo a armazenagem das nossas memórias.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;Mesmo sendo algo tão comum ainda há várias dúvidas sobre a segurança desses sistemas. Você confia a nuvem?<br>
